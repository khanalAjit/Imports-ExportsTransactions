    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        //getting the selected row
        int selectedRow = jTable1.getSelectedRow();
        DefaultTableModel value = (DefaultTableModel) jTable1.getModel(); // using default table model to access our table

        //getting the values from table and insertig them to their respective fields
        nameFld.setText(value.getValueAt(selectedRow, 0).toString());

        String transType = value.getValueAt(selectedRow, 1).toString();
        if (transType.equals("Import")) {
            importRB.setSelected(true);
        } else {
            exportRB.setSelected(true);
        }

        manuFld.setText(value.getValueAt(selectedRow, 2).toString());

        String selectedCate = value.getValueAt(selectedRow, 3).toString();
        switch (selectedCate) {
            case "Food":
                inputCB.setSelectedIndex(0);
                break;
            case "Petroleum":
                inputCB.setSelectedIndex(1);
                break;
            case "Vehicle":
                inputCB.setSelectedIndex(2);
                break;
            case "Jewellery":
                inputCB.setSelectedIndex(3);
                break;
            case "Gadgets":
                inputCB.setSelectedIndex(4);
                break;
            case "Clothes":
                inputCB.setSelectedIndex(5);
                break;
        }

        quantityFld.setText(value.getValueAt(selectedRow, 4).toString());

        priceFld.setText(value.getValueAt(selectedRow, 5).toString());

        dateFld.setText(value.getValueAt(selectedRow, 6).toString());

    }//GEN-LAST:event_jTable1MouseClicked

    private void updateBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateBtnActionPerformed
        // TODO add your handling code here:
        //usign default table model to access our table
        DefaultTableModel changeValue = (DefaultTableModel) jTable1.getModel();
        int selectedRow = jTable1.getSelectedRow(); //getting the selected row

        if (selectedRow >= 0) {
            //changing the table details with new inputs
            changeValue.setValueAt(nameFld.getText(), selectedRow, 0);
            changeValue.setValueAt(buttonGroup2.getSelection().getActionCommand(), selectedRow, 1);
            changeValue.setValueAt(manuFld.getText(), selectedRow, 2);
            changeValue.setValueAt(inputCB.getSelectedItem(), selectedRow, 3);
            changeValue.setValueAt(quantityFld.getText(), selectedRow, 4);
            changeValue.setValueAt(priceFld.getText(), selectedRow, 5);
            changeValue.setValueAt(dateFld.getText(), selectedRow, 6);
            int priceCalc = Integer.parseInt(priceFld.getText()) * Integer.parseInt(quantityFld.getText());
            String totalPrice = Integer.toString(priceCalc);
            changeValue.setValueAt(totalPrice, selectedRow, 7);
            JOptionPane.showMessageDialog(null, "The details have been updated successfully.");
        } else {
            JOptionPane.showMessageDialog(null, "Please select a row to update.");
        }
    }//GEN-LAST:event_updateBtnActionPerformed

