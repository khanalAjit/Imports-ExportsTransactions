    private void priceSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_priceSearchActionPerformed
        // TODO add your handling code here:
        try {
            ArrayList<Integer> totalPrice = new ArrayList();   //creating new Arraylist
            ArrayList<String> all = getValue();                //getting all the table data from getValue() method
            int a = 7;
            for (int i = 0; i < all.size() / 8; i++) {        //getting values of total price and add to totalPrice Arraylist
                totalPrice.add(Integer.parseInt(all.get(a)));
                a += 8;
            }
            ArrayList<Integer> sorted = SortSearch.sort(totalPrice, all);   //passing the arrys to Sort Search class for sorting 

            SortSearch.binarySearch(sorted, Integer.parseInt(searchFld.getText())); // passing the arrys to Sort Search class for searching

            int b = 7;

            for (int i = 0; i < all.size() / 8; i++) {                             //displaying the item with the searched price
                if (all.get(b).equals(searchFld.getText())) {
                    JOptionPane.showMessageDialog(null, "The item with price as " + searchFld.getText() + " is " + all.get(b - 7));
                    break;
                }
                b += 8;
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Please enter the searching price.");
        }
    }//GEN-LAST:event_priceSearchActionPerformed
    public void check(String cateogary, String transactionType) {            //method to check whether the input cateogary exsist in table and display their details
        ArrayList<String> hero = getValue();
        int a = 1;
        int b = 3;
        ArrayList<String> value = new ArrayList();
        for (int i = 0; i < hero.size() / 8; i++) {                           //checking the input
            int c = a;
            if (transactionType.equals(hero.get(a)) && cateogary.equals(hero.get(b))) {
                value.add(hero.get(a - 1));
                for (int j = 0; j <= 6; j++) {
                    value.add(hero.get(c++));                                //all values are stored in the value arraylist
                }
            }
            a += 8;
            b += 8; 
        }
        if (value.isEmpty()) {                                                //if their are no such values
            JOptionPane.showMessageDialog(null, "No such values are present in the table");
        } else {                                                              //if their exsist such value search and display their names
            int d = 0;
            String allItems = "";
            for (int i = 0; i < value.size() / 8; i++) {
                allItems += value.get(d) + " , ";
                d += 8;
            }

            JOptionPane.showMessageDialog(null, "The items which are"
                    + " " + transactionType + "ed " + " and is of " + cateogary + " cateogary " + " are " + allItems);
            value.clear();
        }

    }

    public ArrayList getValue() {                                              //method to store all the values of table into arraylist
        ArrayList<String> data = new ArrayList();
        for (int i = 0; i < jTable1.getRowCount(); i++) {
            for (int j = 0; j < jTable1.getColumnCount(); j++) {
                data.add((String) jTable1.getValueAt(i, j));
            }
        }
        return data;
    }

