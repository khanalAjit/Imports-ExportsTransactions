    private void loadBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loadBtnActionPerformed
        // TODO add your handling code here:
        String path = "E:\\Year 2\\Sem 1\\Emerging Programming Platforms and Technologies\\Coursework 1\\SAR_Transactions\\data.txt"; // the path of the required file
        File file = new File(path); // creating a file for the given fath

        try {
            //file reader to read the file required
            FileReader fReader = new FileReader(file);
            BufferedReader bReader = new BufferedReader(fReader);   //buffer reader to read what's in the file

            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();        //default table model to identify our jtable and to add more functions
            Object[] lines = bReader.lines().toArray();        //table is all in object so creatring object datatype 

            for (int i = 0; i < lines.length; i++) {         //for loop utilization
                String[] row = lines[i].toString().split(",");      //spliting the data in the file into different parts
                model.addRow(row);          //adding the separated data in out jtable.
            }
            JOptionPane.showMessageDialog(null, "Transactions added successfully.");

        } catch (FileNotFoundException ex) {
            JOptionPane.showMessageDialog(null, "File not found",
                    "Error!!", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_loadBtnActionPerformed

