    private void addBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addBtnActionPerformed
        // TODO add your handling code here:
         // getting input data and storing them in local variables
        String name = (String) nameFld.getText();
        String quantity = (String) quantityFld.getText();              
        String price = (String) priceFld.getText();
        String date = (String) dateFld.getText();
        String manufacturer = (String) manuFld.getText();
        String productCateg = (String) inputCB.getSelectedItem();

        //checking the validity of the inputs
        if (name.isEmpty() || quantity.isEmpty() || price.isEmpty() || date.isEmpty() || manufacturer.isEmpty() || buttonGroup2.getSelection() == null) {
            JOptionPane.showMessageDialog(null, "Enter all the fields and select transaction type.");
        } else {
            String transType = buttonGroup2.getSelection().getActionCommand();
            int priceCalc = Integer.parseInt(priceFld.getText()) * Integer.parseInt(quantityFld.getText());
            String totalPrice = Integer.toString(priceCalc);

            String data[] = {name, transType, manufacturer, productCateg, quantity, price, date, totalPrice};   //storing the input data in an array

            DefaultTableModel modelAdd = (DefaultTableModel) jTable1.getModel();        //using default table model to access our table

            modelAdd.addRow(data);      //adding a row in the table with the data of the array.

            JOptionPane.showMessageDialog(null, "Data added successfully.");

            //clearing fields for new inputs.
            nameFld.setText("");
            quantityFld.setText("");
            priceFld.setText("");
            dateFld.setText("");
            manuFld.setText("");
            buttonGroup2.clearSelection();
            //exportRB.setSelected(false);
        }

    }//GEN-LAST:event_addBtnActionPerformed

